        <p class="card-text"> Il s'avère que pour mon dernier roman j'ai tenu à exploiter les nouvelles technologie que l'on nous propose afin de vous faire découvrir mes dernières aventures. Bien que l'histoire soit entièrement fictive, j'ai voulu y intégrer mon expérience acquise au cours de mes nombreux voyages en alaska. C'est pourquoivous dénoterez une certaine véracité dans mon roman. Je suis heureux de partager avec vous cette nouvelle expèrience et de lire vos retours sur la lecture de mon roman.
            Savourez bien !

            Dès son premier ouvrage, Jean Forteroche est récompensé pour ses talents d'écrivain. Son roman Billet pour l'Alaska  ? a reçu le Michael L. Prints Award et fut classé dans le top 10 des meilleurs livres de l'American Library Association.

            Le prix Michael L. Prints Award lui fut à nouveau décerné pour son second roman, Le Théorème des Katherine. Ce dernier était également finaliste pour le Los Angeles Times Book Price.

            En 2008, son roman Flocons d'amour co-écrit avec deux écrivaines est classé 10e sur la liste des bestsellers du New York Times dans la catégorie romans pour enfants. Idem pour son troisième roman, La Face cachée de Margo qui remporte la 5e place, et Nos étoiles contraires classé à la 2e position, cette fois-ci dans la catégorie «romans pour jeunes adultes».

            Deux ans plus tard, son roman co-écrit avec David Levithan, Will & Will reçoit de nombreuses récompenses dont le Stonewall Book Award ainsi que l'Odyssey Award.

            Jean Forteroche vit aujourd'hui avec sa femme Sarah Urist Green et leurs deux enfants Henry et Alice dans sa ville natale d'Indianapolis.
